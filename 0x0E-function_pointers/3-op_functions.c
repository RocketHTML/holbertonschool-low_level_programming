/**
 * op_add - add two ints
 * @a: int a
 * @b: int b
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract two ints
 * @a: int a
 * @b: int b
 * Return: difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two ints
 * @a: int a
 * @b: int b
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two ints
 * @a: int a
 * @b: int b
 * Return: quotient
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - divide two ints
 * @a: int a
 * @b: int b
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
